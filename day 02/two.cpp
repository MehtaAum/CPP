#include <iostream>
using namespace std;

int main() {
// post increment & pre increment
int x = 57;
cout<<endl;

cout<<"After pre increment by 1 the number : ";
cout<<++x<<endl;

cout<<"After post increment by 1 the number : ";
 x++;
cout<<x<<endl;

// post decrememnt & pre decrememnt

cout<<"After pre decrememnt by 1 the number : ";
cout<<--x<<endl;

cout<<"After post decrememnt by 1 the number : ";
x--;
cout<<x--<<endl;


}